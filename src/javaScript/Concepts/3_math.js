// Math.max(): returns largest value
let max = Math.max(1, 5, 3); // output: 5

// Math.abs(): returns absolute value
let abs = Math.abs(-20); // output: 20

// Infinity: represents infinite value
let inf = 1 / 0; // output: Infinity

// NaN: represents not-a-number
let nan = Math.sqrt(-1); // output: NaN
