// JavaScript Math Operators & Math Methods

Operators;

// + Addition
// - Subtraction
// * Multiplication
// / Division
// % Remainder
// ** Exponentiation

// Addition: adds two numbers
let add = 10 + 3; // output: 13

// Subtraction: subtracts one number from another
let sub = 10 - 3; // output: 7

// Multiplication: multiplies two numbers
let mul = 10 * 3; // output: 30

// Division: divides one number by another
let div = 10 / 3; // output: 3.3333333333333335

// Remainder: returns remainder after division
let rem = 10 % 3; // output: 1

// Exponentiation: raises power
let exp = 10 ** 3; // output: 1000

// Math is a built-in JavaScript object that provides mathematical constants and functions.

// Math.pow(): returns power of a number
let power = Math.pow(2, 4); // output: 16

// Math.sqrt(): returns square root
let sqrt = Math.sqrt(16); // output: 4

// Math.floor(): rounds down
let floor = Math.floor(4.9); // output: 4

// Math.ceil(): rounds up
let ceil = Math.ceil(4.1); // output: 5

// Math.round(): rounds to nearest integer
let round = Math.round(4.5); // output: 5

